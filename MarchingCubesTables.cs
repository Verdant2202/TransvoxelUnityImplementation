public struct RegularCellData
{
    public int geometryCounts; // High nibble is vertex count, low nibble is triangle count.
    public byte[] vertices;  // Groups of 3 indexes giving the triangulation.
    public int GetVertexCount()
    {
        return (geometryCounts >> 4);
    }

    public int GetTriangleCount()
    {
        return (geometryCounts & 0x0F);
    }

    public RegularCellData(int geometryCounts, byte[] vertices)
    {
        this.geometryCounts = geometryCounts;
        this.vertices = vertices;
    }
};

// The TransitionCellData structure holds information about the triangulation
// used for a single equivalence class in the Transvoxel Algorithm transition cell,
// described in Section 4.3.
public struct TransitionCellData
{
    public int geometryCounts;        // High nibble is vertex count, low nibble is triangle count.
    public byte[] vertices;  // Groups of 3 indexes giving the triangulation.

    public int GetVertexCount()
    {
        return (geometryCounts >> 4);
    }

    public int GetTriangleCount()
    {
        return (geometryCounts & 0x0F);
    }

    public TransitionCellData(int geometryCounts, byte[] vertices)
    {
        this.geometryCounts = geometryCounts;
        this.vertices = vertices;
    }
};

